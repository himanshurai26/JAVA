package com.FromQNOTwoNine;

public class IndexOf {
    public static void main(String[] args) {
        String name = "Index of Example";

        System.out.println(name.indexOf("of"));
        System.out.println(name.indexOf("e"));
        System.out.println(name.indexOf("Example"));
        System.out.println(name.indexOf("Ind") + " "+ name.indexOf("Ex"));
    }
}
